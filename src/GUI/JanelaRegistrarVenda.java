    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Botao 'OK' <Clicado>
        if (jRadioButton1.isSelected() == true){
            VendaPrazo vp = new VendaPrazo( jcv.getjAnt().getLoja().geraId(), stringToDate(jTextField1.getText()), (Cliente)jComboBox1.getSelectedItem() );
            jcv.getjAnt().getLoja().inserirVenda(vp);
            jcv.insereVendasTabela();
            this.dispose();
        }
        if (jRadioButton2.isSelected() == true){
            VendaVista v = new VendaVista( jcv.getjAnt().getLoja().geraId(), stringToDate(jTextField1.getText()) );
            jcv.getjAnt().getLoja().inserirVenda(v);
            jcv.insereVendasTabela();
            this.dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

