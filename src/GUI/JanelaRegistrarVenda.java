    public Date stringToDate (String s){
	SimpleDateFormat formataData = new SimpleDateFormat("dd/MM/yyyy");
	Date dataEntrada = null;
	try { 
	    dataEntrada = formataData.parse(s);
	} 
        catch (ParseException ex) {
	    ex.printStackTrace();
	}
	return dataEntrada;
	}
    
